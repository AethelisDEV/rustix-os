//! 3. Embedded 8x16 bitmap font loaded via a 16-byte aligned newtype.

/// Represents a high-fidelity 24-bit RGB Color.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct Color {
    /// Red color channel intensity (0..=255).
    pub r: u8,
    /// Green color channel intensity (0..=255).
    pub g: u8,
    /// Blue color channel intensity (0..=255).
    pub b: u8,
}

impl Color {
    /// Creates a new Color from RGB parameters.
    pub const fn new(r: u8, g: u8, b: u8) -> Self {
        Self { r, g, b }
    }
}

